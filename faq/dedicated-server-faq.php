                What is the difference between SAS and SATA?
              </a>
            </div>
            <div id="collapseFour" class="accordion-body collapse">
              <div class="accordion-inner">
                <p>SAS is designed for mission-critical enterprise applications whereas SATA is a general-purpose interface common in the consumer market.</p>
                <ul>
                  <li>SAS supports multiple initiators, while SATA has no such provision.</li>
                  <li>SAS is dual-ported whereas SATA is single ported. SAS is therefore capable of multipath I/O without additional hardware. Moreover, SAS can make use of both ports to scale performance.</li>
                  <li>SATA transfers data at half-duplex while SAS can transfer data at full-duplex. Thus, for SAS, each port can read and write data at the same time whereas SATA cannot.</li>
                  <li>SAS uses SCSI commands for error recovery and error reporting, which have more functionality than the ATA command set used by SATA.</li>
                  <li>SAS can support cables up to 8m long, while the maximum cable length for SATA is 1m.</li>
                </ul>
                <p>The cost of SAS is extra.</p>
